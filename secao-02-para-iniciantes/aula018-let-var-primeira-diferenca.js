/**
 * As variáveis declaradas com 'var' podem ser redeclaradas.
 */

// Não declare variáveis em JS sem antes colocar 'var' ou 'let';
// Dê preferência ao LET;

var nome = 'Erick';
console.log(nome);

var nome = 'João';
console.log(nome);

variavel = "Não crie variável assim!!!";
console.log(variavel);


// ECMAScript (ES6) -> Padronização do JavaScript
/**
 * As variáveis declaradas com 'let' não podem ser redeclaradas
 */

let usuario = 'Erick';
console.log(usuario);
